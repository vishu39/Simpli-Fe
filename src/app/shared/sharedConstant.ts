export const convertKeysToLowercase = () => {
  const newObj = {};
  for (const key in countryColorCode) {
    if (countryColorCode.hasOwnProperty(key)) {
      newObj[key.toLowerCase()] = countryColorCode[key];
    }
  }
  return newObj;
};
